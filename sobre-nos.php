 Quitanda, Tom Jobim I e II) para Empoderamento e emancipação para geração de renda e emprego para a juventude local, dando-lhes assim ferramentas para a superação da vulnerabilidade a longo prazo, e também um caminho na contra mão do trafico de drogas e da violência predominante no contexto social local. </p>
